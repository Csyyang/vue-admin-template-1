          class="login-form"
          auto-complete="on"
          label-position="left"
        >

          <el-form-item prop="password">
            <span class="svg-container">
              <svg-icon icon-class="password" />
            </span>
            <el-input
              :key="passwordType"
              ref="password"
              v-model="loginForm.password"
              :type="passwordType"
              placeholder="Password"
              name="password"
              tabindex="2"
              auto-complete="on"
              @keyup.enter.native="handleLogin"
            />
            <span class="show-pwd" @click="showPwd">
              <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
            </span>
          </el-form-item>

        </el-form>
        <div slot="footer" class="dialog-footer">
          <el-button @click="dialogFormVisible = false">取 消</el-button>
          <el-button :loading="loading" type="primary" @click.native.prevent="handleLogin">确 定</el-button>
        </div>
      </el-dialog>

      <el-dialog key="register" title="添加用户" :visible.sync="dialogUserFormVisible">
        <register ref="register" />
        <div slot="footer" class="dialog-footer">
          <el-button @click="dialogUserFormVisible = false">取 消</el-button>
          <el-button :loading="loading" type="primary" @click.native.prevent="handleUserLogin">确 定</el-button>
        </div>
      </el-dialog>
    </div>
  </div>
</template>

<script>
import { mapGetters } from 'vuex'
import Breadcrumb from '@/components/Breadcrumb'
import Hamburger from '@/components/Hamburger'
import { modify } from '@/api/user'
import register from '@/views/login/register.vue'

export default {
  components: {
    Breadcrumb,
    Hamburger,
    register
  },
  data() {
    return {
      dialogFormVisible: false,
      dialogUserFormVisible: false,
      loginForm: {
        password: ''
      },
      loginRules: {
        username: [{ required: true, trigger: 'blur', message: '请输入用户名' }],
        password: [{ required: true, trigger: 'blur', message: '请输入密码' }]
      },
      passwordType: 'password',
      loading: false
    }
  },
  computed: {
    ...mapGetters([
      'sidebar',
      'avatar'
    ])
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    open() {
      this.dialogFormVisible = true
    },
    openAddUser() {
      this.dialogUserFormVisible = true
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          modify(this.loginForm).then(() => {
            this.$message({
              type: 'success',
              message: '密码修改成功,请重新登录'
            })

            this.logout()
          }).finally(() => {
            this.loading = false
          })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    },
    handleUserLogin() {
      this.$refs.register.handleLogin().then(res => {
        if (res) {
          this.dialogUserFormVisible = false
        }
      })
    },
    toggleSideBar() {
      this.$store.dispatch('app/toggleSideBar')
    },
    async logout() {
      await this.$store.dispatch('user/logout')
      this.$router.push(`/login?redirect=${this.$route.fullPath}`)
    }
  }
}
</script>

